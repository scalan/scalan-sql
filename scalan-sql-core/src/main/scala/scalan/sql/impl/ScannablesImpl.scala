// Abs -----------------------------------
trait ScannablesAbs extends Scalan with Scannables {
  self: ScannablesDsl with ScalanSql =>

  // single proxy for each type family
  implicit def proxyScannable[TableRow, SourceRow](p: Rep[Scannable[TableRow, SourceRow]]): Scannable[TableRow, SourceRow] = {
    proxyOps[Scannable[TableRow, SourceRow]](p)(scala.reflect.classTag[Scannable[TableRow, SourceRow]])
  }

  // familyElem
  class ScannableElem[TableRow, SourceRow, To <: Scannable[TableRow, SourceRow]](implicit _eTableRow: Elem[TableRow], _eSourceRow: Elem[SourceRow])
    extends EntityElem[To] {
    def eTableRow = _eTableRow
    def eSourceRow = _eSourceRow
    lazy val parent: Option[Elem[_]] = None
    lazy val typeArgs = TypeArgs("TableRow" -> eTableRow, "SourceRow" -> eSourceRow)
    override def isEntityType = true
    override lazy val tag = {
      implicit val tagTableRow = eTableRow.tag
      implicit val tagSourceRow = eSourceRow.tag
      weakTypeTag[Scannable[TableRow, SourceRow]].asInstanceOf[WeakTypeTag[To]]
    }
    override def convert(x: Rep[Def[_]]) = {
      implicit val eTo: Elem[To] = this
      val conv = fun {x: Rep[Scannable[TableRow, SourceRow]] => convertScannable(x) }
      tryConvert(element[Scannable[TableRow, SourceRow]], this, x, conv)
    }

    def convertScannable(x: Rep[Scannable[TableRow, SourceRow]]): Rep[To] = {
      x.selfType1 match {
        case _: ScannableElem[_, _, _] => x.asRep[To]
        case e => !!!(s"Expected $x to have ScannableElem[_, _, _], but got $e", x)
      }
    }

    override def getDefaultRep: Rep[To] = ???
  }

  implicit def scannableElement[TableRow, SourceRow](implicit eTableRow: Elem[TableRow], eSourceRow: Elem[SourceRow]): Elem[Scannable[TableRow, SourceRow]] =
    cachedElem[ScannableElem[TableRow, SourceRow, Scannable[TableRow, SourceRow]]](eTableRow, eSourceRow)

  implicit case object ScannableCompanionElem extends CompanionElem[ScannableCompanionAbs] {
    lazy val tag = weakTypeTag[ScannableCompanionAbs]
    protected def getDefaultRep = Scannable
  }

  abstract class ScannableCompanionAbs extends CompanionDef[ScannableCompanionAbs] {
    def selfType = ScannableCompanionElem
    override def toString = "Scannable"
  }
  def Scannable: Rep[ScannableCompanionAbs]
  implicit def proxyScannableCompanionAbs(p: Rep[ScannableCompanionAbs]): ScannableCompanionAbs =
    proxyOps[ScannableCompanionAbs](p)

  abstract class AbsTableScannable[Row]
      (table: Rep[Table], scanId: Rep[Int], direction: Rep[SortDirection], fakeDep: Rep[Unit], kernelInput: Rep[KernelInput])(implicit eRow: Elem[Row])
    extends TableScannable[Row](table, scanId, direction, fakeDep, kernelInput) with Def[TableScannable[Row]] {
    lazy val selfType = element[TableScannable[Row]]
  }
  // elem for concrete class
  class TableScannableElem[Row](val iso: Iso[TableScannableData[Row], TableScannable[Row]])(implicit val eRow: Elem[Row])
    extends ScannableElem[Row, Row, TableScannable[Row]]
    with ConcreteElem[TableScannableData[Row], TableScannable[Row]] {
    override lazy val parent: Option[Elem[_]] = Some(scannableElement(element[Row], element[Row]))
    override lazy val typeArgs = TypeArgs("Row" -> eRow)

    override def convertScannable(x: Rep[Scannable[Row, Row]]) = // Converter is not generated by meta
!!!("Cannot convert from Scannable to TableScannable: missing fields List(direction)")
    override def getDefaultRep = TableScannable(element[Table].defaultRepValue, 0, element[SortDirection].defaultRepValue, (), element[KernelInput].defaultRepValue)
    override lazy val tag = {
      implicit val tagRow = eRow.tag
      weakTypeTag[TableScannable[Row]]
    }
  }

  // state representation type
  type TableScannableData[Row] = (Table, (Int, (SortDirection, (Unit, KernelInput))))

  // 3) Iso for concrete class
  class TableScannableIso[Row](implicit eRow: Elem[Row])
    extends EntityIso[TableScannableData[Row], TableScannable[Row]] with Def[TableScannableIso[Row]] {
    override def from(p: Rep[TableScannable[Row]]) =
      (p.table, p.scanId, p.direction, p.fakeDep, p.kernelInput)
    override def to(p: Rep[(Table, (Int, (SortDirection, (Unit, KernelInput))))]) = {
      val Pair(table, Pair(scanId, Pair(direction, Pair(fakeDep, kernelInput)))) = p
      TableScannable(table, scanId, direction, fakeDep, kernelInput)
    }
    lazy val eFrom = pairElement(element[Table], pairElement(element[Int], pairElement(element[SortDirection], pairElement(element[Unit], element[KernelInput]))))
    lazy val eTo = new TableScannableElem[Row](self)
    lazy val selfType = new TableScannableIsoElem[Row](eRow)
    def productArity = 1
    def productElement(n: Int) = eRow
  }
  case class TableScannableIsoElem[Row](eRow: Elem[Row]) extends Elem[TableScannableIso[Row]] {
    def isEntityType = true
    def getDefaultRep = reifyObject(new TableScannableIso[Row]()(eRow))
    lazy val tag = {
      implicit val tagRow = eRow.tag
      weakTypeTag[TableScannableIso[Row]]
    }
    lazy val typeArgs = TypeArgs("Row" -> eRow)
  }
  // 4) constructor and deconstructor
  class TableScannableCompanionAbs extends CompanionDef[TableScannableCompanionAbs] {
    def selfType = TableScannableCompanionElem
    override def toString = "TableScannable"
    @scalan.OverloadId("fromData")
    def apply[Row](p: Rep[TableScannableData[Row]])(implicit eRow: Elem[Row]): Rep[TableScannable[Row]] =
      isoTableScannable(eRow).to(p)
    @scalan.OverloadId("fromFields")
    def apply[Row](table: Rep[Table], scanId: Rep[Int], direction: Rep[SortDirection], fakeDep: Rep[Unit], kernelInput: Rep[KernelInput])(implicit eRow: Elem[Row]): Rep[TableScannable[Row]] =
      mkTableScannable(table, scanId, direction, fakeDep, kernelInput)

    def unapply[Row](p: Rep[Scannable[Row, Row]]) = unmkTableScannable(p)
  }
  lazy val TableScannableRep: Rep[TableScannableCompanionAbs] = new TableScannableCompanionAbs
  lazy val TableScannable: TableScannableCompanionAbs = proxyTableScannableCompanion(TableScannableRep)
  implicit def proxyTableScannableCompanion(p: Rep[TableScannableCompanionAbs]): TableScannableCompanionAbs = {
    proxyOps[TableScannableCompanionAbs](p)
  }

  implicit case object TableScannableCompanionElem extends CompanionElem[TableScannableCompanionAbs] {
    lazy val tag = weakTypeTag[TableScannableCompanionAbs]
    protected def getDefaultRep = TableScannable
  }

  implicit def proxyTableScannable[Row](p: Rep[TableScannable[Row]]): TableScannable[Row] =
    proxyOps[TableScannable[Row]](p)

  implicit class ExtendedTableScannable[Row](p: Rep[TableScannable[Row]])(implicit eRow: Elem[Row]) {
    def toData: Rep[TableScannableData[Row]] = isoTableScannable(eRow).from(p)
  }

  // 5) implicit resolution of Iso
  implicit def isoTableScannable[Row](implicit eRow: Elem[Row]): Iso[TableScannableData[Row], TableScannable[Row]] =
    reifyObject(new TableScannableIso[Row]()(eRow))

  // 6) smart constructor and deconstructor
  def mkTableScannable[Row](table: Rep[Table], scanId: Rep[Int], direction: Rep[SortDirection], fakeDep: Rep[Unit], kernelInput: Rep[KernelInput])(implicit eRow: Elem[Row]): Rep[TableScannable[Row]]
  def unmkTableScannable[Row](p: Rep[Scannable[Row, Row]]): Option[(Rep[Table], Rep[Int], Rep[SortDirection], Rep[Unit], Rep[KernelInput])]

  abstract class AbsIndexScannable[Row, IndexRow]
      (table: Rep[Table], index: Rep[Index], scanId: Rep[Int], direction: Rep[SortDirection], fakeDep: Rep[Unit], kernelInput: Rep[KernelInput])(implicit eRow: Elem[Row], eIndexRow: Elem[IndexRow])
    extends IndexScannable[Row, IndexRow](table, index, scanId, direction, fakeDep, kernelInput) with Def[IndexScannable[Row, IndexRow]] {
    lazy val selfType = element[IndexScannable[Row, IndexRow]]
  }
  // elem for concrete class
  class IndexScannableElem[Row, IndexRow](val iso: Iso[IndexScannableData[Row, IndexRow], IndexScannable[Row, IndexRow]])(implicit val eRow: Elem[Row], val eIndexRow: Elem[IndexRow])
    extends ScannableElem[Row, IndexRow, IndexScannable[Row, IndexRow]]
    with ConcreteElem[IndexScannableData[Row, IndexRow], IndexScannable[Row, IndexRow]] {
    override lazy val parent: Option[Elem[_]] = Some(scannableElement(element[Row], element[IndexRow]))
    override lazy val typeArgs = TypeArgs("Row" -> eRow, "IndexRow" -> eIndexRow)

    override def convertScannable(x: Rep[Scannable[Row, IndexRow]]) = // Converter is not generated by meta
!!!("Cannot convert from Scannable to IndexScannable: missing fields List(index, direction)")
    override def getDefaultRep = IndexScannable(element[Table].defaultRepValue, element[Index].defaultRepValue, 0, element[SortDirection].defaultRepValue, (), element[KernelInput].defaultRepValue)
    override lazy val tag = {
      implicit val tagRow = eRow.tag
      implicit val tagIndexRow = eIndexRow.tag
      weakTypeTag[IndexScannable[Row, IndexRow]]
    }
  }

  // state representation type
  type IndexScannableData[Row, IndexRow] = (Table, (Index, (Int, (SortDirection, (Unit, KernelInput)))))

  // 3) Iso for concrete class
  class IndexScannableIso[Row, IndexRow](implicit eRow: Elem[Row], eIndexRow: Elem[IndexRow])
    extends EntityIso[IndexScannableData[Row, IndexRow], IndexScannable[Row, IndexRow]] with Def[IndexScannableIso[Row, IndexRow]] {
    override def from(p: Rep[IndexScannable[Row, IndexRow]]) =
      (p.table, p.index, p.scanId, p.direction, p.fakeDep, p.kernelInput)
    override def to(p: Rep[(Table, (Index, (Int, (SortDirection, (Unit, KernelInput)))))]) = {
      val Pair(table, Pair(index, Pair(scanId, Pair(direction, Pair(fakeDep, kernelInput))))) = p
      IndexScannable(table, index, scanId, direction, fakeDep, kernelInput)
    }
    lazy val eFrom = pairElement(element[Table], pairElement(element[Index], pairElement(element[Int], pairElement(element[SortDirection], pairElement(element[Unit], element[KernelInput])))))
    lazy val eTo = new IndexScannableElem[Row, IndexRow](self)
    lazy val selfType = new IndexScannableIsoElem[Row, IndexRow](eRow, eIndexRow)
    def productArity = 2
    def productElement(n: Int) = n match {
      case 0 => eRow
      case 1 => eIndexRow
    }
  }
  case class IndexScannableIsoElem[Row, IndexRow](eRow: Elem[Row], eIndexRow: Elem[IndexRow]) extends Elem[IndexScannableIso[Row, IndexRow]] {
    def isEntityType = true
    def getDefaultRep = reifyObject(new IndexScannableIso[Row, IndexRow]()(eRow, eIndexRow))
    lazy val tag = {
      implicit val tagRow = eRow.tag
      implicit val tagIndexRow = eIndexRow.tag
      weakTypeTag[IndexScannableIso[Row, IndexRow]]
    }
    lazy val typeArgs = TypeArgs("Row" -> eRow, "IndexRow" -> eIndexRow)
  }
  // 4) constructor and deconstructor
  class IndexScannableCompanionAbs extends CompanionDef[IndexScannableCompanionAbs] {
    def selfType = IndexScannableCompanionElem
    override def toString = "IndexScannable"
    @scalan.OverloadId("fromData")
    def apply[Row, IndexRow](p: Rep[IndexScannableData[Row, IndexRow]])(implicit eRow: Elem[Row], eIndexRow: Elem[IndexRow]): Rep[IndexScannable[Row, IndexRow]] =
      isoIndexScannable(eRow, eIndexRow).to(p)
    @scalan.OverloadId("fromFields")
    def apply[Row, IndexRow](table: Rep[Table], index: Rep[Index], scanId: Rep[Int], direction: Rep[SortDirection], fakeDep: Rep[Unit], kernelInput: Rep[KernelInput])(implicit eRow: Elem[Row], eIndexRow: Elem[IndexRow]): Rep[IndexScannable[Row, IndexRow]] =
      mkIndexScannable(table, index, scanId, direction, fakeDep, kernelInput)

    def unapply[Row, IndexRow](p: Rep[Scannable[Row, IndexRow]]) = unmkIndexScannable(p)
  }
  lazy val IndexScannableRep: Rep[IndexScannableCompanionAbs] = new IndexScannableCompanionAbs
  lazy val IndexScannable: IndexScannableCompanionAbs = proxyIndexScannableCompanion(IndexScannableRep)
  implicit def proxyIndexScannableCompanion(p: Rep[IndexScannableCompanionAbs]): IndexScannableCompanionAbs = {
    proxyOps[IndexScannableCompanionAbs](p)
  }

  implicit case object IndexScannableCompanionElem extends CompanionElem[IndexScannableCompanionAbs] {
    lazy val tag = weakTypeTag[IndexScannableCompanionAbs]
    protected def getDefaultRep = IndexScannable
  }

  implicit def proxyIndexScannable[Row, IndexRow](p: Rep[IndexScannable[Row, IndexRow]]): IndexScannable[Row, IndexRow] =
    proxyOps[IndexScannable[Row, IndexRow]](p)

  implicit class ExtendedIndexScannable[Row, IndexRow](p: Rep[IndexScannable[Row, IndexRow]])(implicit eRow: Elem[Row], eIndexRow: Elem[IndexRow]) {
    def toData: Rep[IndexScannableData[Row, IndexRow]] = isoIndexScannable(eRow, eIndexRow).from(p)
  }

  // 5) implicit resolution of Iso
  implicit def isoIndexScannable[Row, IndexRow](implicit eRow: Elem[Row], eIndexRow: Elem[IndexRow]): Iso[IndexScannableData[Row, IndexRow], IndexScannable[Row, IndexRow]] =
    reifyObject(new IndexScannableIso[Row, IndexRow]()(eRow, eIndexRow))

  // 6) smart constructor and deconstructor
  def mkIndexScannable[Row, IndexRow](table: Rep[Table], index: Rep[Index], scanId: Rep[Int], direction: Rep[SortDirection], fakeDep: Rep[Unit], kernelInput: Rep[KernelInput])(implicit eRow: Elem[Row], eIndexRow: Elem[IndexRow]): Rep[IndexScannable[Row, IndexRow]]
  def unmkIndexScannable[Row, IndexRow](p: Rep[Scannable[Row, IndexRow]]): Option[(Rep[Table], Rep[Index], Rep[Int], Rep[SortDirection], Rep[Unit], Rep[KernelInput])]

  registerModule(Scannables_Module)
}

// Std -----------------------------------
trait ScannablesStd extends ScalanStd with ScannablesDsl {
  self: ScannablesDsl with ScalanSqlStd =>

  lazy val Scannable: Rep[ScannableCompanionAbs] = new ScannableCompanionAbs {
  }

  case class StdTableScannable[Row]
      (override val table: Rep[Table], override val scanId: Rep[Int], override val direction: Rep[SortDirection], override val fakeDep: Rep[Unit], override val kernelInput: Rep[KernelInput])(implicit eRow: Elem[Row])
    extends AbsTableScannable[Row](table, scanId, direction, fakeDep, kernelInput) {
  }

  def mkTableScannable[Row]
    (table: Rep[Table], scanId: Rep[Int], direction: Rep[SortDirection], fakeDep: Rep[Unit], kernelInput: Rep[KernelInput])(implicit eRow: Elem[Row]): Rep[TableScannable[Row]] =
    new StdTableScannable[Row](table, scanId, direction, fakeDep, kernelInput)
  def unmkTableScannable[Row](p: Rep[Scannable[Row, Row]]) = p match {
    case p: TableScannable[Row] @unchecked =>
      Some((p.table, p.scanId, p.direction, p.fakeDep, p.kernelInput))
    case _ => None
  }

  case class StdIndexScannable[Row, IndexRow]
      (override val table: Rep[Table], override val index: Rep[Index], override val scanId: Rep[Int], override val direction: Rep[SortDirection], override val fakeDep: Rep[Unit], override val kernelInput: Rep[KernelInput])(implicit eRow: Elem[Row], eIndexRow: Elem[IndexRow])
    extends AbsIndexScannable[Row, IndexRow](table, index, scanId, direction, fakeDep, kernelInput) {
  }

  def mkIndexScannable[Row, IndexRow]
    (table: Rep[Table], index: Rep[Index], scanId: Rep[Int], direction: Rep[SortDirection], fakeDep: Rep[Unit], kernelInput: Rep[KernelInput])(implicit eRow: Elem[Row], eIndexRow: Elem[IndexRow]): Rep[IndexScannable[Row, IndexRow]] =
    new StdIndexScannable[Row, IndexRow](table, index, scanId, direction, fakeDep, kernelInput)
  def unmkIndexScannable[Row, IndexRow](p: Rep[Scannable[Row, IndexRow]]) = p match {
    case p: IndexScannable[Row, IndexRow] @unchecked =>
      Some((p.table, p.index, p.scanId, p.direction, p.fakeDep, p.kernelInput))
    case _ => None
  }
}

